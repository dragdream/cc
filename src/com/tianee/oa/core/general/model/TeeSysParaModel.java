package com.tianee.oa.core.general.model;

public class TeeSysParaModel {
	
	private String uuid;
	private String paraValue;
	private String paraName;
	private String ifParent;
	private String paraParent;
	
	
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public String getParaValue() {
		return paraValue;
	}
	public void setParaValue(String paraValue) {
		this.paraValue = paraValue;
	}
	public String getParaName() {
		return paraName;
	}
	public void setParaName(String paraName) {
		this.paraName = paraName;
	}
	public String getIfParent() {
		return ifParent;
	}
	public void setIfParent(String ifParent) {
		this.ifParent = ifParent;
	}
	public String getParaParent() {
		return paraParent;
	}
	public void setParaParent(String paraParent) {
		this.paraParent = paraParent;
	}
	
	

}
