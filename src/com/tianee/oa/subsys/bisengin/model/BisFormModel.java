package com.tianee.oa.subsys.bisengin.model;


public class BisFormModel {
	
	private int sid;
	private String formName;
	private String content;
	private String shortContent;
	private int sortNo;
	private int bisTableId;
	private String bisTableName;
	
	
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getFormName() {
		return formName;
	}
	public void setFormName(String formName) {
		this.formName = formName;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getShortContent() {
		return shortContent;
	}
	public void setShortContent(String shortContent) {
		this.shortContent = shortContent;
	}
	public int getSortNo() {
		return sortNo;
	}
	public void setSortNo(int sortNo) {
		this.sortNo = sortNo;
	}
	public int getBisTableId() {
		return bisTableId;
	}
	public void setBisTableId(int bisTableId) {
		this.bisTableId = bisTableId;
	}
	public String getBisTableName() {
		return bisTableName;
	}
	public void setBisTableName(String bisTableName) {
		this.bisTableName = bisTableName;
	}
	
	
}
