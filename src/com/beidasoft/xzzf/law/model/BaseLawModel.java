package com.beidasoft.xzzf.law.model;


public class BaseLawModel {
	    private String id;

	    private String name; // 名称

	    private String timeliness; // 时效性

	    private String submitlawLevel; // 法律法规类别

	    private String word; // 发文字号

	    private String organ; // 发布机关

	    private String promulgationStr; // 颁布日期
 
	    private String implementationStr; // 实施日期

	    private String remark; // 备注

	    private String examine; // 审核状态

	    private String isDelete; // 是否删除

	    private String createId; // 创建者

	    private String createTime; // 创建时间
 
	    private String lawPath; // 法律原文文件路径

	    private String lawNum; // 法律编号

	    private String annulTimeStr; // 废止日期

		public String getId() {
			return id;
		}

		public void setId(String id) {
			this.id = id;
		}

		public String getName() {
			return name;
		}

		public void setName(String name) {
			this.name = name;
		}

		public String getTimeliness() {
			return timeliness;
		}

		public void setTimeliness(String timeliness) {
			this.timeliness = timeliness;
		}

		public String getSubmitlawLevel() {
			return submitlawLevel;
		}

		public void setSubmitlawLevel(String submitlawLevel) {
			this.submitlawLevel = submitlawLevel;
		}

		public String getWord() {
			return word;
		}

		public void setWord(String word) {
			this.word = word;
		}

		public String getOrgan() {
			return organ;
		}

		public void setOrgan(String organ) {
			this.organ = organ;
		}

		public String getPromulgationStr() {
			return promulgationStr;
		}

		public void setPromulgationStr(String promulgationStr) {
			this.promulgationStr = promulgationStr;
		}

		public String getImplementationStr() {
			return implementationStr;
		}

		public void setImplementationStr(String implementationStr) {
			this.implementationStr = implementationStr;
		}

		public String getRemark() {
			return remark;
		}

		public void setRemark(String remark) {
			this.remark = remark;
		}

		public String getExamine() {
			return examine;
		}

		public void setExamine(String examine) {
			this.examine = examine;
		}

		public String getIsDelete() {
			return isDelete;
		}

		public void setIsDelete(String isDelete) {
			this.isDelete = isDelete;
		}

		public String getCreateId() {
			return createId;
		}

		public void setCreateId(String createId) {
			this.createId = createId;
		}

		public String getCreateTime() {
			return createTime;
		}

		public void setCreateTime(String createTime) {
			this.createTime = createTime;
		}

		public String getLawPath() {
			return lawPath;
		}

		public void setLawPath(String lawPath) {
			this.lawPath = lawPath;
		}

		public String getLawNum() {
			return lawNum;
		}

		public void setLawNum(String lawNum) {
			this.lawNum = lawNum;
		}

		public String getAnnulTimeStr() {
			return annulTimeStr;
		}

		public void setAnnulTimeStr(String annulTimeStr) {
			this.annulTimeStr = annulTimeStr;
		}
	    
	    
}
