package com.beidasoft.xzfy.caseTrial.caseHearing.model;

public class HearingModel {
	
	private String id;
	
	private String caseId;
	
	private String hearingWitness;
	
	private String place;
	
	private String startTime;
	
	private String endTime;
	
	private String participant;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCaseId() {
		return caseId;
	}

	public void setCaseId(String caseId) {
		this.caseId = caseId;
	}

	public String getHearingWitness() {
		return hearingWitness;
	}

	public void setHearingWitness(String hearingWitness) {
		this.hearingWitness = hearingWitness;
	}

	public String getPlace() {
		return place;
	}

	public void setPlace(String place) {
		this.place = place;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public String getParticipant() {
		return participant;
	}

	public void setParticipant(String participant) {
		this.participant = participant;
	}
	
	
	
}
